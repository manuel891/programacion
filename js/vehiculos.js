	function obtener_datos(id){
			var marca_vehiculo = $("#marca"+id).val();
			var modelo_vehiculo = $("#modelo"+id).val();
			var año_vehiculo = $("#año"+id).val();
	
			$("#mod_marca").val(marca_vehiculo);
			$("#mod_modelo").val(modelo_vehiculo);
			$("#mod_year").val(año_vehiculo);
			$("#mod_id").val(id);
		
		}
	
		
		

